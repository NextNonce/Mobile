package com.nextnonce.app.core.enums

/**
 * Represents the sign of a number.
 *
 * This enum is used to categorize numbers into three distinct groups:
 * - NEGATIVE: The number is less than zero.
 * - ZERO: The number is exactly zero.
 * - POSITIVE: The number is greater than zero.
 */
enum class NumberSign {
    NEGATIVE,   // Value is less than zero
    ZERO,       // Value is exactly zero
    POSITIVE    // Value is greater than zero
}
